# parte 1
print('Operatores aritméticos')

print('O somatório dos 5 primeiros números inteiros e positivos:')
print(1 + 2 + 3 + 4 + 4)

print('A média entre 23, 19 e 31:')
print((23 + 19 + 31) / 3)

print('O número de vezes que 73 cabe em 403:')
print(403 // 73)

print('A sobra quando 403 é dividido por 73:')
print(403 % 73)

print('2 elevado a 10ª potência')
print(2 ** 10)

print('O valor absoluto da diferença entre 54 e 57')
print(abs(54 - 57))

print('O menor valor entre 34, 29 e 31')
print(min(34, 29, 31))

